import json
import random

# 1Ô∏è‚É£ Load questions from JSON file
with open("questions.json", "r") as file:
    questions = json.load(file)

# 2Ô∏è‚É£ Shuffle the questions
random.shuffle(questions)

# 3Ô∏è‚É£ Start score
score = 0

# 4Ô∏è‚É£ Ask each question
for i, q in enumerate(questions, start=1):
    print(f"\nQ{i}. {q['question']}")
    
    # Print options
    for index, option in enumerate(q["options"], start=1):
        print(f"{index}. {option}")
    
    try:
        choice = int(input("Your answer (1-4): "))
        user_answer = q["options"][choice - 1]
        
        if user_answer.lower() == q["answer"].lower():
            print("‚úÖ Correct!")
            score += 1
        else:
            print(f"‚ùå Wrong! Correct answer: {q['answer']}")
    
    except:
        print("‚ö†Ô∏è Invalid input. Skipping this question.")

# 5Ô∏è‚É£ Final score
print("\nüéâ Quiz finished!")
print(f"üèÅ Your total score is: {score} out of {len(questions)}")
